def generate_stats(position, rating):
    stats = {}

    for stat, weight in stats_weights[position].items():
        mean = 109 - (109 - rating) * 0.5
        std_dev = 10  # Desviaci√≥n est√°ndar de la distribuci√≥n
        value = int(np.random.normal(mean * weight, std_dev) )
        value = max(value, 0)  # Asegurarse de que no sea negativo
        value = min(value, 99)  # Asegurarse de que est√© entre 0 y 99
        stats[stat] = value
    # print()
    return stats